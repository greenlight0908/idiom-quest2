function buildPosChoices(answer,index){
  const rest=POS_LABELS.filter(label=>label!==answer);
  const choices=[answer,rest[index%rest.length],rest[(index*3+1)%rest.length],rest[(index*5+2)%rest.length]];
  const unique=[...new Set(choices)];
  for(const label of rest)if(unique.length<4&&!unique.includes(label))unique.push(label);
  const shift=index%4,rotated=unique.slice(shift).concat(unique.slice(0,shift));
  return {opts:rotated,a:rotated.indexOf(answer)};
}
function posTopic(word){
  const last=word.charCodeAt(word.length-1),hasBatchim=last>=0xAC00&&last<=0xD7A3&&(last-0xAC00)%28>0;
  return hasBatchim?"은":"는";
}

let posQuestionNumber=0;
const POS_QUESTION_BANK=POS_GROUPS.flatMap(group=>group.entries.map(entry=>{
  const number=posQuestionNumber++,choice=buildPosChoices(group.answer,number);
  return {
    q:`${entry[0]} 이 문장에서 ‘${entry[1]}’의 품사는?`,
    opts:choice.opts,
    a:choice.a,
    e:`‘${entry[1]}’${posTopic(entry[1])} ${entry[2]} 따라서 품사는 ${group.answer}이다.`
  };
}));
if(POS_QUESTION_BANK.length!==100)throw new Error(`품사 문항 수 오류: ${POS_QUESTION_BANK.length}`);
